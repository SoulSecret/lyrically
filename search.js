	let search_box = document.getElementById('input');
	let search_bx2 = document.getElementsByClassName('search_bx2')[0];
	
window.addEventListener('load', () => {
				song.forEach(element =>{
								const { img, title, artist, url } = element;
								
								let card = document.createElement('a');
								card.href = url;
								card.innerHTML = `<img src="${img}">
													<div class="artistName">
													<h6>${title}</h6>
													<p>${artist}</p>
									</div>`;
									search_bx2.appendChild(card);
				});
				
});
	          	search_box.addEventListener('keyup', () => {
								let filter = search_box.value.toLowerCase();
								let a = search_bx2.getElementsByTagName('a');
								for (let i = 0; i < a.length; i++) {
												let b = a[i].getElementsByClassName('artistName')[0];
												let c = b.getElementsByTagName('h6')[0];
												let d = b.getElementsByTagName('p')[0];
												
												let title = c.textContent || c.innerText;
												let artist = d.textContent || d.innerText;
												if (title.toLowerCase().indexOf(filter) > -1) {
																a[i].style.display = "";
																search_bx2.style.visibility = "visible";
																search_bx2.style.opacity = 1;
												 } else if (artist.toLowerCase().indexOf(filter) > -1) {
																a[i].style.display = "";
																search_bx2.style.visibility = "visible";
																search_bx2.style.opacity = 1;
												} else {
																a[i].style.display = "none";
												}
												if (search_box.value == 0) {
																search_bx2.style.visibility = "hidden";
																search_bx2.style.opacity = 0;
												}
								}
				});
